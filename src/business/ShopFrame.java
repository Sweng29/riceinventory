    private void jshopsearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jshopsearchKeyReleased
      String jshopSearch = jshopsearch.getText();
        TableRowSorter tableRowSorter = new TableRowSorter(jshoptable.getModel());
        jshoptable.setRowSorter(tableRowSorter);
        tableRowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + jshopSearch));
    }//GEN-LAST:event_jshopsearchKeyReleased

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
      int id = Integer.parseInt(jshoptable.getValueAt(jshoptable.getSelectedRow(),0 ).toString());
        ShopModel shopModel = new ShopModel();
        shopModel.setShopId(id);
      if(jshoptable.getSelectedRow()>-1){
      int row = new ShopDaoImpl().deleteShopData(shopModel);
      if(row>0){
          new MessageForm("Success","Deleted","info.png").setVisible(true);
          fillShopTable();
      
      }else{
      new MessageForm("UnSuccess","Not Deleted","error.png").setVisible(true);
      }
      }else{
      new MessageForm("Please Select Row!!!","UnSuccess","error.png").setVisible(true);
      }
      
    }//GEN-LAST:event_DeleteActionPerformed

