    private void addshopMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addshopMouseClicked
        
        if(jshopname.getText().equals("")||jshopquantity.getText().equals("")){
        
       new MessageForm("UnSuccuss","All Fields Are Required To Fill","error.png").setVisible(true);     
        }else{
        String shopName = jshopname.getText();
        String shopQuantity = jshopquantity.getText();
        Timestamp currentTime = new Timestamp(System.currentTimeMillis());
        ShopModel shopModel  = new ShopModel();
        shopModel.setName(shopName);
        shopModel.setShopQty(shopQuantity);
        shopModel.setCreatedDate(currentTime);
        shopModel.setModifiedDate(currentTime);
        shopModel.setModifiedBy(LoginFrame.employeesModel);
        shopModel.setCreatedBy(LoginFrame.employeesModel);
        
        ShopDaoImpl shopDaoImpl = new ShopDaoImpl();
        int row = shopDaoImpl.insertShopData(shopModel);
        if(row>0){
       new MessageForm("Succuss","Inserted","info.png").setVisible(true);
        fillShopTable();
        }else{
        new MessageForm("UnSuccess","Not Inserted","error.png").setVisible(true);
        }
        }
        
        
    }//GEN-LAST:event_addshopMouseClicked

    private void updateshopMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateshopMouseClicked
        if(jshopname.getText().equals("")||jshopquantity.getText().equals("")){
        
       new MessageForm("UnSuccuss","All Fields Are Required To Fill","error.png").setVisible(true)   ;  
        }else{
        int id = Integer.parseInt(jshoptable.getValueAt(jshoptable.getSelectedRow(), 0).toString());
        
        String shopName = jshopname.getText();
        String shopQuantity = jshopquantity.getText();
        Timestamp currentTime = new Timestamp(System.currentTimeMillis());
        ShopModel shopModel  = new ShopModel();
        shopModel.setName(shopName);
        shopModel.setShopQty(shopQuantity);
        shopModel.setModifiedDate(currentTime);
        shopModel.setModifiedBy(LoginFrame.employeesModel);
        shopModel.setShopId(id);
        ShopDaoImpl shopDaoImpl = new ShopDaoImpl();
        int row = shopDaoImpl.updateShopData(shopModel);
        if(row>0){
        new MessageForm("Succuss","Updated","info.png").setVisible(true);
        fillShopTable();
        }else{
        new MessageForm("UnSuccuss","Not Updated","error.png").setVisible(true);
        
        }
        }
    }//GEN-LAST:event_updateshopMouseClicked

