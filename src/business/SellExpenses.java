    private void comboPaymentTypeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboPaymentTypeItemStateChanged
        if (!comboPaymentType.getSelectedItem().toString().equals("Cash") && !comboPaymentType.getSelectedItem().toString().equals("Select Payment Type")) {
            jDialog1.setSize(new Dimension(687, 320));
            jDialog1.setLocationRelativeTo(accTitle);
            jDialog1.setVisible(true);

        } else if (comboPaymentType.getSelectedItem().toString().equals("Cash") || comboPaymentType.getSelectedItem().toString().equals("Select Payment Type")) {
            accNo.setText("");
            accTitle.setText("");
        }
    }//GEN-LAST:event_comboPaymentTypeItemStateChanged

    private void bankTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bankTableMouseClicked
        if (evt.getClickCount() == 2) {
            try {
                int bankId = (int) bankTable.getValueAt(bankTable.getSelectedRow(), 0);
                bankModel.setBankId(bankId);
                accNo.setText((String) bankTable.getValueAt(bankTable.getSelectedRow(), 2));
                accTitle.setText((String) bankTable.getValueAt(bankTable.getSelectedRow(), 1));
                jDialog1.dispose();
            } catch (Exception e) {
                e.printStackTrace();
                new MessageForm("Error", "Please select bank", "info.png").setVisible(true);
            }
        }
    }//GEN-LAST:event_bankTableMouseClicked

