    private void deletePurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletePurchaseActionPerformed
        int row = tblPurchase.getSelectedRow();
        if (row > -1) {
            if (tblPurchase.getValueAt(row, 3).toString().equals("Online") || tblPurchase.getValueAt(row, 3).toString().equals("Cheque")) {
            int expenseId = (int) tblPurchase.getValueAt(row, 0);
            BankModel bankModel = new BankModel();
            bankModel.setAccountNo((String) tblPurchase.getValueAt(row, 2));
            purchaseExpenseModel.setExpenseId(expenseId);
            purchaseExpenseModel.setTransportExpense((Integer) tblPurchase.getValueAt(row, 4));
            purchaseExpenseModel.setLabourExpense((Integer) tblPurchase.getValueAt(row, 5));
            purchaseExpenseModel.setOther((Integer) tblPurchase.getValueAt(row, 6));
            purchaseExpenseModel.setBrokerPaidAmount((Integer) tblPurchase.getValueAt(row, 8));
            purchaseExpenseModel.setComment(note.getText());
            purchaseExpenseModel.setBank(bankModel);

            int status = sellExpenseDaoImpl.deletePurchaseExpense(purchaseExpenseModel);
            if (status == 1) {
                CommonMethods.fillTables(sellExpenseDaoImpl.getAllPurchaseExpense(sellModel), tblPurchase, jScrollPane2, this);
            }
        }
            else if(tblPurchase.getValueAt(row, 3).toString().equals("Cash")){
           
            int expenseId = (int) tblPurchase.getValueAt(row, 0);
            purchaseExpenseModel.setExpenseId(expenseId);
        
            int status = sellExpenseDaoImpl.deletePurchaseExpenseWithoutBank(purchaseExpenseModel);
            if (status == 1) {
                CommonMethods.fillTables(sellExpenseDaoImpl.getAllPurchaseExpense(sellModel), tblPurchase, jScrollPane2, this);
            } 
            }
        }
        else {
            new MessageForm("Error", "Please select the expense", "error.png").setVisible(true);
        }
    }//GEN-LAST:event_deletePurchaseActionPerformed

