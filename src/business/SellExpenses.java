    private void addExpenseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addExpenseMouseClicked
        if (comboPaymentType.getSelectedItem().toString().equals("Online") || comboPaymentType.getSelectedItem().toString().equals("Cheque")) {
            if (!transport.getText().equals("") && !labour.getText().equals("") && !other.getText().equals("") && !brokerComission.getText().equals("") && !brokerPaid.getText().equals("") && !accNo.getText().equals("") && !accTitle.getText().equals("")) {
                purchaseExpenseModel.setTransportExpense(Integer.parseInt(transport.getText()));
                purchaseExpenseModel.setLabourExpense(Integer.parseInt(labour.getText()));
                purchaseExpenseModel.setOther(Integer.parseInt(other.getText()));
                purchaseExpenseModel.setBrokerComission(Integer.parseInt(brokerComission.getText()));
                purchaseExpenseModel.setBrokerPaidAmount(Integer.parseInt(brokerPaid.getText()));
                purchaseExpenseModel.setComment(note.getText());
                purchaseExpenseModel.setBank(bankModel);
                purchaseExpenseModel.setSellModel(sellModel);
                purchaseExpenseModel.setPaymentType(comboPaymentType.getSelectedItem().toString());
                purchaseExpenseModel.setCreatedBy(LoginFrame.employeesModel);

                int status = sellExpenseDaoImpl.insertPurchaseExpense(purchaseExpenseModel);
                if (status == 1) {
                    CommonMethods.fillTables(sellExpenseDaoImpl.getAllPurchaseExpense(sellModel), tblPurchase, jScrollPane2, this);
                }
            }
        } else if (comboPaymentType.getSelectedItem().toString().equals("Cash")) {
            if (!transport.getText().equals("") && !labour.getText().equals("") && !other.getText().equals("") && !brokerComission.getText().equals("") && !brokerPaid.getText().equals("")) {
                purchaseExpenseModel.setTransportExpense(Integer.parseInt(transport.getText()));
                purchaseExpenseModel.setLabourExpense(Integer.parseInt(labour.getText()));
                purchaseExpenseModel.setOther(Integer.parseInt(other.getText()));
                purchaseExpenseModel.setBrokerComission(Integer.parseInt(brokerComission.getText()));
                purchaseExpenseModel.setBrokerPaidAmount(Integer.parseInt(brokerPaid.getText()));
                purchaseExpenseModel.setComment(note.getText());
                purchaseExpenseModel.setSellModel(sellModel);
                purchaseExpenseModel.setPaymentType(comboPaymentType.getSelectedItem().toString());
                purchaseExpenseModel.setCreatedBy(LoginFrame.employeesModel);

                int status = sellExpenseDaoImpl.insertPurchaseExpenseWithoutBank(purchaseExpenseModel);
                if (status == 1) {
                    CommonMethods.fillTables(sellExpenseDaoImpl.getAllPurchaseExpense(sellModel), tblPurchase, jScrollPane2, this);
                }
            }
        } else if (comboPaymentType.getSelectedItem().toString().equals("Select Payment Type") && accNo.getText().equals("") && accTitle.getText().equals("")) {
            new MessageForm("Error", "Please select payment method", "error.png").setVisible(true);
        }
    }//GEN-LAST:event_addExpenseMouseClicked

